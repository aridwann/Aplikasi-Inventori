    private void printPerperiodeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_printPerperiodeMouseClicked
        if(!(dc1.getDate() == null && dc2.getDate() == null)){
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd"); 

            String start = dateFormat.format(dc1.getDate());
            String end = dateFormat.format(dc2.getDate());
            service.laporan2(pnTampilLaporan, "LaporanBarangMasukPerperiode", start, end);

        }else{
            JOptionPane.showMessageDialog(null, "Pilih Tanggal Terlebih Dahulu");
        }
    }//GEN-LAST:event_printPerperiodeMouseClicked

    private void printPerbulanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_printPerbulanMouseClicked
        if(!(cyear.getYear()== -1 && cmonth.getMonth() == -1)){
            int selectedMonth = cmonth.getMonth();
            Calendar calendar = Calendar.getInstance();
            calendar.set(Calendar.MONTH, selectedMonth);

            SimpleDateFormat monthFormat = new SimpleDateFormat("MM");
            String month = monthFormat.format(calendar.getTime());
            String year = Integer.toString(cyear.getYear());
            String param = year + "-" + month + "%";
            service.laporan1(pnTampilLaporan, "LaporanBarangMasukPerbulan", param);
        }else{
            JOptionPane.showMessageDialog(null, "Pilih Tanggal Terlebih Dahulu");
        }
    }//GEN-LAST:event_printPerbulanMouseClicked

