    private void cbPilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbPilihActionPerformed
        String pil = (String) cbPilih.getSelectedItem();
        switch (pil){
            case "--Pilih Jenis Laporan--":
                pnAction.setVisible(true);
                pnPertransaksi.setVisible(false);
                pnPerdistributor.setVisible(false);
                pnPerperiode.setVisible(false);
                pnPerbulan.setVisible(false);
                break;
            case "Pertransaksi":
                pnAction.setVisible(false);
                pnPertransaksi.setVisible(true);
                pnPerdistributor.setVisible(false);
                pnPerperiode.setVisible(false);
                pnPerbulan.setVisible(false);
                break;
            case "Perdistributor":
                pnAction.setVisible(false);
                pnPertransaksi.setVisible(false);
                pnPerdistributor.setVisible(true);
                pnPerperiode.setVisible(false);
                pnPerbulan.setVisible(false);
                break;
            case "Perperiode":
                pnAction.setVisible(false);
                pnPertransaksi.setVisible(false);
                pnPerdistributor.setVisible(false);
                pnPerperiode.setVisible(true);
                pnPerbulan.setVisible(false);
                break;
            case "Perbulan":
                pnAction.setVisible(false);
                pnPertransaksi.setVisible(false);
                pnPerdistributor.setVisible(false);
                pnPerperiode.setVisible(false);
                pnPerbulan.setVisible(true);
                break;
            default:
                break;
        }
    }//GEN-LAST:event_cbPilihActionPerformed

